Home Funerals and Natural/Green Burials</h1>

 
<h3>
National Home Funeral Alliance</h3>
<p>
This site includes emergency contact information and resources for every state in the U.S. "The mission of the NHFA is to educate and empower families and communities to care for their own dead."</p>

<h3>
Green Burial Council</h3>
<p>
