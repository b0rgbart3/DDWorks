End-of-Life Planning
</h1>
 
<h3>
National Hospice and Palliative Care Organization</h3>
<p>

This is one of the very best comprehensive websites I've found so far. The resources are extensive and easy to navigate including downloadable Advance Care Directives by State. "... a program of the National Hospice and Palliative Care Organization, provides free resources to help people make decisions about end-of-life care and services before a crisis."</p>

<h3>The Order of the Good Death</h3>
<p>
Founded by an alternative funeral director, this website comprehensively addresses all aspects of death and dying with phenomenal resources. “The members of the Order believe there is a revolution afoot in the way our society handles death…We invite you to join the Order and make the changes that will allow you, your family, and your culture die better.”</p>
<p><a href='http://www.orderofthegooddeath.com/'>http://www.orderofthegooddeath.com/</a></p>
 

<h3>Cake</h3>
<p>
"Make your ending meaningful and easier on loved ones. Explore, document and share all your health, legal, funeral and legacy decisions in an end-of-life plan. Make it easier for family in the future."</p>
