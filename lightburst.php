<h2>LightBurst</h2>
<p>This simple game is surprisingly challenging and fun.  The game consists of a square grid of lights. When the game starts, a random pattern of these lights is switched on. Pressing any of the lights will toggle it and the adjacent lights. The goal of the puzzle is to switch all the lights off, preferably in as few button presses as possible.</p>
<p>The player can choose the number of tiles in the grid, the color of the grid and the length and therefore the complexity and difficulty of the sequence.</p>
<p>The game is available for all iOS devices, on the App Store.</p>
